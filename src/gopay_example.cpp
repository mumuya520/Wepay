#include "gopay_client.hpp" // 包含 GoPay 客户端头文件
#include <iostream> // 标准输入输出库

using namespace gopay; // 使用 gopay 命名空间

// 主函数：GoPay 客户端示例程序
int main() {
    Client client; // 创建 GoPay 客户端实例

    // ═══════════════════════════════════════════════════════════════
    // 启动 GoPay 服务
    // ═══════════════════════════════════════════════════════════════
    std::cout << "启动 GoPay 服务..." << std::endl; // 打印启动消息
    if (!client.start("", "0.0.0.0", "9090")) { // 启动服务，监听 0.0.0.0:9090
        std::cerr << "启动失败" << std::endl; // 启动失败则打印错误
        return 1; // 返回错误码
    }

    std::cout << "服务地址: " << client.addr() << std::endl; // 打印服务地址
    std::cout << "版本: " << client.version() << std::endl; // 打印版本号

    // ═══════════════════════════════════════════════════════════════
    // 测试用户注册
    // ═══════════════════════════════════════════════════════════════
    auto reg_resp = client.user_register("test@example.com", "13800138000", "123456"); // 注册新用户
    if (reg_resp.ok()) { // 如果注册成功
        std::cout << "用户注册成功: " << reg_resp.data.dump(2) << std::endl; // 打印注册结果
    } else { // 否则打印错误信息
        std::cout << "注册失败: " << reg_resp.msg << std::endl;
    }

    // ═══════════════════════════════════════════════════════════════
    // 测试用户登录
    // ═══════════════════════════════════════════════════════════════
    auto login_resp = client.user_login(10000, "123456"); // 用户登录
    if (login_resp.ok()) { // 如果登录成功
        std::cout << "登录成功, token: " << login_resp.data["token"] << std::endl; // 打印 token
    } else { // 否则打印错误信息
        std::cout << "登录失败: " << login_resp.msg << std::endl;
    }

    // ═══════════════════════════════════════════════════════════════
    // 测试创建订单
    // ═══════════════════════════════════════════════════════════════
    auto order_resp = client.order_create(
        10000, // 用户 ID
        "TEST" + std::to_string(time(nullptr)), // 外部订单号（使用时间戳确保唯一）
        "测试商品", // 商品名称
        "http://localhost/notify", // 异步通知 URL
        100.50, // 金额
        1, // 支付类型（1 表示支付宝）
        1, // 通道 ID
        "127.0.0.1" // 客户端 IP
    );

    if (order_resp.ok()) { // 如果订单创建成功
        std::cout << "订单创建成功: " << order_resp.data.dump(2) << std::endl; // 打印订单信息
        std::string trade_no = order_resp.data["trade_no"]; // 获取订单号

        // ═══════════════════════════════════════════════════════════════
        // 查询订单
        // ═══════════════════════════════════════════════════════════════
        auto query_resp = client.order_get(trade_no); // 按订单号查询
        if (query_resp.ok()) { // 如果查询成功
            std::cout << "订单查询: " << query_resp.data.dump(2) << std::endl; // 打印订单详情
        }
    } else { // 否则打印错误信息
        std::cout << "订单创建失败: " << order_resp.msg << std::endl;
    }

    // ═══════════════════════════════════════════════════════════════
    // 查询账户余额
    // ═══════════════════════════════════════════════════════════════
    auto balance_resp = client.balance_get(10000); // 查询用户余额
    if (balance_resp.ok()) { // 如果查询成功
        std::cout << "账户余额: " << balance_resp.data["balance"] << std::endl; // 打印余额
    }

    // ═══════════════════════════════════════════════════════════════
    // 获取订单列表
    // ═══════════════════════════════════════════════════════════════
    auto list_resp = client.order_list(10000, 1, 10); // 获取用户订单列表（第 1 页，每页 10 条）
    if (list_resp.ok()) { // 如果查询成功
        std::cout << "订单总数: " << list_resp.data["total"] << std::endl; // 打印订单总数
        std::cout << "订单列表: " << list_resp.data["list"].dump(2) << std::endl; // 打印订单列表
    }

    // ═══════════════════════════════════════════════════════════════
    // 等待用户输入后停止服务
    // ═══════════════════════════════════════════════════════════════
    std::cout << "\n按回车键停止服务..." << std::endl; // 提示用户
    std::cin.get(); // 等待用户按回车键

    // ═══════════════════════════════════════════════════════════════
    // 停止 GoPay 服务
    // ═══════════════════════════════════════════════════════════════
    client.stop(); // 停止服务
    std::cout << "服务已停止" << std::endl; // 打印停止消息

    return 0; // 返回成功
}
